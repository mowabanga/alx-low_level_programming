#include <stdio.h>
#include <time.h>
/**More headers can be included here
 * Return will be zero 
 * Program assigns a variable each time it is executed
 */
int main()
{
	char n;
	printf("Please put in number \n");
	scanf("%d", &n);

	if (n = 0, n > 0)
	{
	printf("%d is positive\n", n);
	}
	else if (n = 0)
	{
	printf("%d is zero\n", n);
	}
	else (n < 0);
	{
	printf("%d is negative\n", n);
	}

	return (0);
}
